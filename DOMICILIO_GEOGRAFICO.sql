CREATE TABLE `DOMICILIO_GEOGRAFICO` (
  `DOMICILIO_ID` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `CVE_TIPO_DOM` decimal(10,0) NOT NULL,
  `CVE_ENT` varchar(2) NOT NULL,
  `CVE_MUN` varchar(3) NOT NULL,
  `CVE_LOC` varchar(4) NOT NULL,
  `CVE_PERIODO` decimal(2,0) NOT NULL,
  `NUMEXTNUM` decimal(5,0) DEFAULT NULL,
  `NUMEXTALF` varchar(35) DEFAULT NULL,
  `NUMEXT_ANT` varchar(35) DEFAULT NULL,
  `NUMINTNUM` decimal(5,0) DEFAULT NULL,
  `NUMINTALF` varchar(35) DEFAULT NULL,
  `CVE_ASEN` varchar(13) NOT NULL,
  `CODIGO` varchar(240) NOT NULL,
  `CVE_VIA_REF1` decimal(10,0) DEFAULT NULL,
  `CVE_VIA_REF2` decimal(10,0) DEFAULT NULL,
  `CVE_VIA_REF3` decimal(10,0) DEFAULT NULL,
  `DESCRIPC` varchar(255) DEFAULT NULL,
  `DOM_GEOG` varchar(255) DEFAULT NULL,
  `FECHA_HORA_ALTA` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`DOMICILIO_ID`),
  UNIQUE KEY `DOMICILIO_ID` (`DOMICILIO_ID`),
  KEY `DGO_CTM_FK` (`CVE_TIPO_DOM`),
  KEY `DGO_CAOS_FK` (`CVE_ASEN`,`CVE_MUN`,`CVE_LOC`,`CVE_ENT`,`CVE_PERIODO`),
  KEY `DGO_VIDD_FK3` (`CVE_VIA_REF3`),
  KEY `DGO_VIDD_FK2` (`CVE_VIA_REF2`),
  KEY `DGO_VIDD_FK` (`CVE_VIA_REF1`),
  KEY `DGO_LOD_FK` (`CVE_MUN`,`CVE_ENT`,`CVE_LOC`,`CVE_PERIODO`),
  CONSTRAINT `DGO_CAOS_FK` FOREIGN KEY (`CVE_ASEN`, `CVE_MUN`, `CVE_LOC`, `CVE_ENT`, `CVE_PERIODO`) REFERENCES `ASENTAMIENTO` (`CVE_ASEN`, `CVE_MUN`, `CVE_LOC`, `CVE_ENT`, `CVE_PERIODO`),
  CONSTRAINT `DGO_CTM_FK` FOREIGN KEY (`CVE_TIPO_DOM`) REFERENCES `CAT_TIPO_DOM` (`CVE_TIPO_DOM`),
  CONSTRAINT `DGO_LOD_FK` FOREIGN KEY (`CVE_MUN`, `CVE_ENT`, `CVE_LOC`, `CVE_PERIODO`) REFERENCES `CAT_LOCALIDAD` (`CVE_MUN`, `CVE_ENT`, `CVE_LOC`, `CVE_PERIODO`),
  CONSTRAINT `DGO_VIDD_FK` FOREIGN KEY (`CVE_VIA_REF1`) REFERENCES `VIALIDAD` (`CVE_VIA`),
  CONSTRAINT `DGO_VIDD_FK2` FOREIGN KEY (`CVE_VIA_REF2`) REFERENCES `VIALIDAD` (`CVE_VIA`),
  CONSTRAINT `DGO_VIDD_FK3` FOREIGN KEY (`CVE_VIA_REF3`) REFERENCES `VIALIDAD` (`CVE_VIA`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;